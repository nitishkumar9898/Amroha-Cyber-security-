/**
 * =============================================================================
 * K6 LOAD TEST — Investigation Pipeline Performance
 * =============================================================================
 *
 * Tests:
 *   - Evidence upload throughput (MB/s)
 *   - Investigation pipeline latency (p50, p95, p99)
 *   - Concurrent investigation capacity
 *   - Module health under load
 *   - Chain-of-custody write throughput
 *
 * Run: k6 run --vus 50 --duration 5m tests/load/k6-pipeline-test.js
 */

import http from 'k6/http';
import { check, sleep, group } from 'k6';
import { Rate, Trend, Counter } from 'k6/metrics';

// ── Custom Metrics ──────────────────────────────────────────────────────────

const pipelineLatency = new Trend('pipeline_latency_ms');
const uploadThroughput = new Rate('upload_throughput_mbps');
const investigationSuccess = new Rate('investigation_success_rate');
const chainCustodyLatency = new Trend('chain_custody_latency_ms');
const totalIOCDiscovered = new Counter('total_iocs_discovered');

// ─── Configuration ──────────────────────────────────────────────────────────

const BASE_URL = __ENV.BASE_URL || 'http://localhost:3000/api/v1';
const TOKEN = __ENV.AUTH_TOKEN || 'test-token-placeholder';

const AUTH_HEADERS = {
  'Authorization': `Bearer ${TOKEN}`,
  'Content-Type': 'application/json',
};

const SAMPLE_EVIDENCE_TYPES = [
  'device_image', 'log_file', 'memory_dump',
  'network_capture', 'malware_sample', 'document',
];

// ─── Setup ───────────────────────────────────────────────────────────────────

export function setup() {
  // Create a test case
  const caseResp = http.post(`${BASE_URL}/cases`, JSON.stringify({
    title: `Load Test Case ${Date.now()}`,
    description: 'Generated by k6 load test',
    classification: 'MEDIUM',
    type: 'cyber_crime',
    jurisdiction: 'LOAD_TEST',
  }), { headers: AUTH_HEADERS });

  check(caseResp, { 'case created': (r) => r.status === 201 });
  const caseId = caseResp.json('id');
  return { caseId };
}

// ─── Main Test ───────────────────────────────────────────────────────────────

export default function (data: { caseId: string }) {
  const { caseId } = data;

  group('Evidence Upload', () => {
    const evidenceType = SAMPLE_EVIDENCE_TYPES[Math.floor(Math.random() * SAMPLE_EVIDENCE_TYPES.length)];

    const payload = {
      caseId,
      evidenceType,
      description: `Load test upload - ${evidenceType}`,
      metadata: { testRun: Date.now(), virtualUser: __VU },
    };

    const startTime = Date.now();
    const resp = http.post(`${BASE_URL}/evidence/upload`, JSON.stringify(payload), {
      headers: AUTH_HEADERS,
    });
    const latency = Date.now() - startTime;

    pipelineLatency.add(latency);
    uploadThroughput.add(resp.body.length / (1024 * 1024) / (latency / 1000));

    check(resp, {
      'evidence uploaded': (r) => r.status === 201,
      'evidence has hash': (r) => r.json('hash') !== undefined,
    });
  });

  group('Create Investigation', () => {
    const investResp = http.post(`${BASE_URL}/investigations`, JSON.stringify({
      caseId,
      evidenceIds: [], // Would reference actual evidence IDs
      pipelineId: 'standard-investigation',
    }), { headers: AUTH_HEADERS });

    investigationSuccess.add(investResp.status === 201);

    check(investResp, {
      'investigation created': (r) => r.status === 201,
      'has investigation id': (r) => r.json('investigationId') !== undefined,
    });
  });

  group('Chain-of-Custody Record', () => {
    const startTime = Date.now();
    // Simulate a custody event
    const custodyResp = http.post(`${BASE_URL}/evidence/chain-of-custody`, JSON.stringify({
      evidenceId: `load-test-evidence-${__VU}`,
      action: 'accessed',
      notes: 'Load test access',
    }), { headers: AUTH_HEADERS });
    const latency = Date.now() - startTime;

    chainCustodyLatency.add(latency);

    check(custodyResp, {
      'custody recorded': (r) => r.status === 200 || r.status === 201,
    });
  });

  group('System Health Check', () => {
    const healthResp = http.get(`${BASE_URL}/system/health`, { headers: AUTH_HEADERS });

    check(healthResp, {
      'health endpoint ok': (r) => r.status === 200,
      'system is healthy': (r) => r.json('status') !== 'critical',
    });

    if (healthResp.json('modulesByStatus')?.error > 0) {
      console.warn(`VU ${__VU}: Module errors detected`);
    }
  });

  sleep(Math.random() * 2 + 0.5); // Simulate realistic inter-arrival time
}

// ─── Teardown ───────────────────────────────────────────────────────────────

export function teardown(data: { caseId: string }) {
  // Clean up test case
  http.del(`${BASE_URL}/cases/${data.caseId}`, null, { headers: AUTH_HEADERS });
}

// ─── Thresholds ─────────────────────────────────────────────────────────────

export const options = {
  stages: [
    { duration: '1m', target: 10 },   // Ramp up
    { duration: '3m', target: 50 },   // Steady
    { duration: '1m', target: 0 },    // Ramp down
  ],
  thresholds: {
    'pipeline_latency_ms': ['p(95)<5000', 'p(99)<10000'],
    'investigation_success_rate': ['rate>0.95'],
    'chain_custody_latency_ms': ['p(95)<500'],
    'http_req_duration': ['p(95)<3000'],
    'http_req_failed': ['rate<0.01'],
  },
};
